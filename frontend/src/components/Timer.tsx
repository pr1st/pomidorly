import React from 'react';
import {TimerConfigState, TimerState} from "../types/timer";
import classNames from "classnames";
import "bootstrap/dist/css/bootstrap.css"
import TimerConfigDialog from "./TimerConfigDialog";
import "bootstrap"

export type StateProps = TimerState

export type DispatchProps = {
    nextSecond: () => void,
    startTimer: () => void,
    pauseTimer: () => void,
    stopTimer: () => void,
    skipTimer: () => void,
    setConfig: (timerConfig: TimerConfigState) => void,
    doPomidor: () => void
}

let interval : NodeJS.Timeout;
let isRunning = false;

const Timer = ( props: StateProps & DispatchProps) => {
    const { nextSecond, pauseTimer, skipTimer, startTimer, stopTimer, setConfig, doPomidor} = props;
    const { numberOfPomidorsBeforeLongBreak,  alarmWhenZero} = props.config;
    const {isBreak, currentPomidor, time, isActive} = props.currentState;

    if (isActive && !isRunning) {
        isRunning = true;
        interval = setInterval(() => {
            clearInterval(interval);
            isRunning = false;
            if (time === 1) {
                if (alarmWhenZero) {
                    beepSound.play();
                }
                if (!isBreak) {
                    doPomidor()
                }
            }
            nextSecond()
        }, 1000)
     }
    const btnClass = classNames("btn", "btn-outline-primary", "btn-lg")
    const min = Math.floor(time/60)
    const sec = time % 60
    const actionState = isActive ? isBreak ? "Break" : "Action" : "Pause"

    return (
        <div className={classNames("Timer")}>
            <div className={"Options"}>
                <button
                    className={classNames(btnClass)}
                    data-toggle="modal"
                    data-target="#timerConfigModal"
                    data-backdrop="static"
                >
                    Config
                </button>
                <TimerConfigDialog {...props.config} setConfig={setConfig}/>
            </div>
            <div className={classNames("Time", actionState)}>{min}:{(sec < 10) ? "0"+sec : sec}</div>
            <h5>{actionState}</h5>
            <p>Pomidors: {currentPomidor}/{numberOfPomidorsBeforeLongBreak}</p>
            <div>
                <button
                    className={btnClass}
                    onClick={startTimer}
                >
                    Start
                </button>
                <button
                    className={btnClass}
                    onClick={pauseTimer}
                >
                    Pause
                </button>
                <button
                    className={btnClass}
                    onClick={stopTimer}
                >
                    Stop
                </button>
                <button
                    className={btnClass}
                    onClick={e => {
                        skipTimer()
                        if (!isBreak) {
                            doPomidor()
                        }
                    }}
                >
                    Skip
                </button>
            </div>
        </div>
    );
}

export default Timer;

